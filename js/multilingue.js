  function cambiarIdioma(lang) {
    document.querySelectorAll('[data-key]').forEach(function(element) {
      const key = element.getAttribute('data-key');
      if (traducciones[key] && traducciones[key][lang]) {
        if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
          element.setAttribute('placeholder', traducciones[key][lang]);
        } else if (element.tagName === 'BUTTON') {
          element.textContent = traducciones[key][lang];
        } else {
          element.innerHTML = traducciones[key][lang];
        }
      }
    });
  
  // Actualizar los mensajes de error
  document.querySelectorAll('[data-error-key]').forEach(function(element) {
    const key = element.getAttribute('data-error-key');
    if (traducciones[key] && traducciones[key][lang]) {
      element.setAttribute('data-error', traducciones[key][lang]);
    }
  });
    localStorage.setItem('selectedLanguage', lang);
  }
  
  // Cargar el idioma almacenado al inicializar la página.
  document.addEventListener('DOMContentLoaded', function() {
    const storedLanguage = localStorage.getItem('selectedLanguage') || 'en';
    cambiarIdioma(storedLanguage);
    
    // Actualizar el menú desplegable con el idioma seleccionado.
    const languageDropdown = document.getElementById('languageDropdown');
    languageDropdown.innerHTML = traducciones['idioma'][storedLanguage];
  });
  
  
  
  